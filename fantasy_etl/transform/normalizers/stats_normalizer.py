# 统计数据标准化器
#
# 迁移来源: @database_writer.py 中的统计数据处理逻辑
# 主要映射:
#   - 统计值字典格式标准化
#   - stat_id到实际统计项的映射逻辑
#
# 职责:
#   - 标准化统计数据格式：
#     * 将API返回的stats数组转换为stat_id->value字典
#     * 处理嵌套的stat结构：stats[i]["stat"]["stat_id/value"]
#   - 统计类别映射：
#     * 将Yahoo stat_id映射到标准统计项名称
#     * 建立统计类别到数据库列的映射关系
#   - 统计值类型转换：
#     * 整数统计：得分、篮板、助攻等
#     * 百分比统计：投篮命中率、罚球命中率等  
#     * 复合统计：投篮made/attempted格式解析
#   - 特殊值处理：
#     * 空值、'-'、'N/A'等无效值处理
#     * 百分比格式标准化：从字符串转为数值
#   - 数据验证：
#     * 统计值合理性检查
#     * 数据类型验证
#
# 输入: Yahoo API stats数组 (List[Dict])
# 输出: 标准化的统计值字典 (Dict[str, Any]) 