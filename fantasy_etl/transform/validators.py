# 数据验证器
#
# 迁移来源: 从旧脚本中的隐式验证逻辑中提取
# 主要职责: 将分散在各个脚本中的数据验证逻辑集中化
#
# 职责:
#   - 核心字段验证：
#     * 必需字段存在性检查：player_key、team_key、league_key等
#     * 字段格式验证：键值格式、日期格式等
#     * 数据类型验证：字符串、整数、布尔值等
#   - 业务规则验证：
#     * 联盟配置合理性：num_teams、playoff配置等  
#     * 球员信息一致性：位置、团队归属等
#     * 统计数据合理性：数值范围、逻辑关系等
#   - 关系约束验证：
#     * 外键关系：team_key存在于teams中等
#     * 数据一致性：日期范围、赛季信息等
#     * 引用完整性：球员-团队关系等
#   - 数据质量检查：
#     * 重复数据检测
#     * 缺失值处理策略
#     * 异常值识别
#   - 验证结果报告：
#     * 错误类型分类
#     * 严重程度评估
#     * 修复建议生成
#
# 输入: 各种标准化后的数据对象
# 输出: 验证结果对象，包含错误列表和警告信息 