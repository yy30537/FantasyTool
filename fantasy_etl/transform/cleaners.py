# 数据清洗器
#
# 迁移来源: 分散在旧脚本中的数据清洗逻辑
# 主要职责: 集中化数据清洗和预处理逻辑
#
# 职责:
#   - 字符串清洗：
#     * 去除多余空白字符
#     * 处理特殊字符和编码问题
#     * 统一字符串格式（大小写等）
#   - 数值清洗：
#     * 处理数值字符串转换
#     * 清理数值中的非数字字符
#     * 处理科学记数法和百分比格式
#   - 日期时间清洗：
#     * 统一日期格式
#     * 处理时区信息
#     * 修复不完整的日期数据
#   - 布尔值清洗：
#     * 统一布尔值表示：'0'/'1' -> False/True
#     * 处理字符串布尔值：'true'/'false'
#     * 处理数字布尔值：0/1
#   - 列表和字典清洗：
#     * 移除空值和无效元素
#     * 扁平化嵌套结构
#     * 处理重复项
#   - 缺失值处理：
#     * 标识和标记缺失值
#     * 应用默认值策略
#     * 缺失值插值和估计
#   - 异常值处理：
#     * 识别和标记异常值
#     * 异常值修正策略
#     * 保留原始值的同时提供清洗值
#
# 输入: 原始或部分处理的数据对象
# 输出: 清洗后的数据对象，保持原始结构 